package codigos;

public class Calculadora {

	public double somar(int n1, int n2) {
		double result = n1 + n2;
		return result;
	}

	public double subtrair(int n1, int n2) {
		double result = n1 - n2;
		return result;
	}

	public double multiplicar(int n1, int n2) {
		double result = n1 * n2;
		return result;
	}

	public double dividir(int n1, int n2) {
		double result = n1 / n2;
		return result;
	}

}
